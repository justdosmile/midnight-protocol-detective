#!/usr/bin/env node

import { randomBytes, webcrypto } from 'node:crypto';
import { readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { mkdir } from 'node:fs/promises';

const encoder = new TextEncoder();
const { subtle } = webcrypto;
const iterations = 150_000;

const normalizeAnswer = (value) =>
  value
    .normalize('NFKC')
    .toLocaleLowerCase('ru-RU')
    .replaceAll('ё', 'е')
    .replace(/[.,;:!?"'«»()[\]{}\-_—–/\\]+/gu, ' ')
    .replace(/\s+/gu, ' ')
    .trim();

const toBase64 = (value) => Buffer.from(value).toString('base64');

async function encryptForAnswer(answer, reveal) {
  const normalized = normalizeAnswer(answer);
  if (!normalized) throw new Error('Вариант ответа не может быть пустым.');

  const sourceKey = await subtle.importKey('raw', encoder.encode(normalized), 'PBKDF2', false, ['deriveKey']);
  const salt = randomBytes(16);
  const iv = randomBytes(12);
  const key = await subtle.deriveKey(
    { name: 'PBKDF2', hash: 'SHA-256', salt, iterations },
    sourceKey,
    { name: 'AES-GCM', length: 256 },
    false,
    ['encrypt'],
  );
  const plaintext = encoder.encode(JSON.stringify(reveal));
  const ciphertext = await subtle.encrypt({ name: 'AES-GCM', iv }, key, plaintext);

  return { salt: toBase64(salt), iv: toBase64(iv), ciphertext: toBase64(ciphertext), iterations };
}

async function main() {
  const inputPath = resolve(process.argv[2] ?? '.private/solution-input.json');
  const outputPath = resolve(process.argv[3] ?? 'src/data/solutionBundle.generated.ts');
  const input = JSON.parse(await readFile(inputPath, 'utf8'));

  if (!Array.isArray(input.acceptedAnswers) || input.acceptedAnswers.length < 2) {
    throw new Error('Нужно указать минимум два допустимых варианта ответа.');
  }
  if (!input.reveal || input.reveal.bundleMarker !== 'MIDNIGHT_PROTOCOL_REVEAL_V1') {
    throw new Error('Финальное досье не прошло проверку формата.');
  }

  const normalizedAnswers = [...new Set(input.acceptedAnswers.map(normalizeAnswer))];
  const payloads = [];
  for (const answer of normalizedAnswers) payloads.push(await encryptForAnswer(answer, input.reveal));

  const source = `// Generated by scripts/generate-solution.mjs. Do not edit by hand.\n` +
    `import type { EncryptedSolutionBundle } from '../types/game';\n\n` +
    `export const encryptedSolutionBundles: EncryptedSolutionBundle[] = ${JSON.stringify(payloads, null, 2)};\n`;

  await mkdir(dirname(outputPath), { recursive: true });
  await writeFile(outputPath, source, 'utf8');
  process.stdout.write(`Encrypted ${payloads.length} answer variants into ${outputPath}\n`);
}

main().catch((error) => {
  process.stderr.write(`${error instanceof Error ? error.message : String(error)}\n`);
  process.exitCode = 1;
});
